#include "holberton.h"
/**
 *_islower - entry point
 *@c: lowercase or uppercase char
 *Description: look for lowercase chars
 *Return: 1 if c is lowercase, otherwise 0
 */
int _islower(int c)
{
	if (c >= 'a' && c <= 'z')
	{
		int c = '1';

		_putchar(c);
	}
	else
	{
		int c = '0';

		_putchar(c);
	}
	return (0);
}

