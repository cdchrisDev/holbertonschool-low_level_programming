/**
 * _strcat - a func that concatenates two strings
 * @dest: string concatenated
 * @src: string to concatenate
 * Return: pointer to dest
 */

char *_strcat(char *dest, char *src)
{
	while (*src++ != '\0')
		;


	while (*src++ = *dest++)
	{
		if (*src || *dest == '\0')
			continue;
	}

	dest = *dest['\0'];

	return (dest);

}
